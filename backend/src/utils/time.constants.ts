export const MINUTE = 60 * 1000; // 60 seconds in milliseconds
export const HOUR = 60 * MINUTE; // 60 minutes in milliseconds
export const DAY = 24 * HOUR; // 24 hours in milliseconds
export const WEEK = 7 * DAY; // 7 days in milliseconds
export const MONTH = 30 * DAY; // 30 days in milliseconds
export const YEAR = 365 * DAY; // 365 days in milliseconds